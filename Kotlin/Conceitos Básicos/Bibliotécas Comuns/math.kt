import kotlin.math.*

fun main() {
    println(max(5, 10)) // valor máximo
    println(min(5, 10)) // valor mínimo
    println(sqrt(53.4343f)) // Rais quadrada
    println(PI) // Número Pi
    println(E) // Número de Euler
    println(round(43530.54465)) // Arredondar
}
